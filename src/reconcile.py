def classify_channels(row: dict) -> pd.DataFrame:
    """渠道初判：列出当月有金额的渠道及其拟入科目 / 处理方式 / 置信度。"""
    out = []
    for r in CHANNEL_RULES:
        amt = float(row.get(r["渠道"], 0) or 0)
        if abs(amt) > 0.01:
            out.append({"渠道": r["渠道"], "金额": round(amt, 2),
                        "拟入科目": r["拟入科目"], "处理方式": r["处理方式"], "置信度": r["置信度"]})
    return pd.DataFrame(out)


def reconcile_wechat(应收: float, plat: dict | None) -> dict | None:
    """微信渠道应收↔实收对账。应收=结算报表，实收=微信平台流水。"""
    if not plat:
        return None
    实收 = plat["实收"]
    差异 = round(实收 - 应收, 2)
    rate = round(差异 / 应收 * 100, 2) if 应收 else 0.0
    return {
        "渠道": "微信POS",
        "结算报表应收": round(应收, 2),
        "平台实收": 实收,
        "手续费/抽佣": plat["手续费"],
        "差异": 差异,
        "差异率%": rate,
        "笔数": plat["笔数"],
        "判定": "一致" if abs(rate) < 1.0 else "需归因",
    }


def reconcile_takeaway(渠道: str, 结算原价: float, plat: dict | None) -> dict | None:
    """外卖渠道对账：结算报表原价 vs 平台实收，差异≈平台抽佣/服务费。"""
    if not plat or plat.get("实收", 0) == 0:
        return None
    实收 = plat["实收"]
    差异 = round(实收 - 结算原价, 2)
    rate = round(差异 / 结算原价 * 100, 2) if 结算原价 else 0.0
    抽佣 = round(结算原价 - 实收, 2)
    return {
        "渠道": 渠道,
        "结算报表应收": round(结算原价, 2),
        "平台实收": 实收,
        "手续费/抽佣": 抽佣,
        "差异": 差异,
        "差异率%": rate,
        "笔数": plat.get("笔数", 0),
        "判定": "差异≈平台抽佣",
    }
